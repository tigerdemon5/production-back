    public GoogleDriveService() throws Exception {
        final NetHttpTransport HTTP_TRANSPORT = GoogleNetHttpTransport.newTrustedTransport();
        GoogleCredentials credentials = getCredentials();
        this.driveService = new Drive.Builder(
                HTTP_TRANSPORT,
                JSON_FACTORY,
                new HttpCredentialsAdapter(credentials)
        ).setApplicationName(APPLICATION_NAME).build();
    }
    private GoogleCredentials getCredentials() throws IOException {
        InputStream in = GoogleDriveService.class.getResourceAsStream(SERVICE_ACCOUNT_KEY_PATH);
        if (in == null) {
            throw new FileNotFoundException("Service account key not found at " + SERVICE_ACCOUNT_KEY_PATH);
        }
        return ServiceAccountCredentials.fromStream(in)
                .createScoped(Collections.singletonList("https://www.googleapis.com/auth/drive"));
    }

    public String uploadFileToDrive(MultipartFile file) {
        try {
            // 1. ÌååÏùº Î©îÌÉÄÎç∞Ïù¥ÌÑ∞ ÏÑ§Ï†ï
            com.google.api.services.drive.model.File fileMetadata = new com.google.api.services.drive.model.File();
            fileMetadata.setName(file.getOriginalFilename());

            // 2. ÌååÏùº ÎÇ¥Ïö© ÏÑ§Ï†ï (application/octet-streamÏùÄ Î≤îÏö©Ïù¥ÏßÄÎßå ÌïÑÏöîÏãú "image/jpeg" Îì±ÏúºÎ°ú ÍµêÏ≤¥ Í∞ÄÎä•)
            InputStreamContent mediaContent = new InputStreamContent(
                    "application/octet-stream", file.getInputStream()
            );

            // 3. ÏóÖÎ°úÎìú ÏöîÏ≤≠ ÏÉùÏÑ±
            Drive.Files.Create createRequest = driveService.files()
                    .create(fileMetadata, mediaContent)
                    .setFields("id, webViewLink");

            // 4. ÏóÖÎ°úÎìú ÏÉÅÌÉú Î°úÍπÖ Î¶¨Ïä§ÎÑà Îì±Î°ù
            createRequest.getMediaHttpUploader().setProgressListener(
                    uploader -> {
                        switch (uploader.getUploadState()) {
                            case INITIATION_STARTED:
                                System.out.println("‚úÖ Upload Initiation Started");
                                break;
                            case INITIATION_COMPLETE:
                                System.out.println("‚úÖ Upload Initiation Complete");
                                break;
                            case MEDIA_IN_PROGRESS:
                                System.out.printf("üîÑ Upload Progress: %.2f%%\n", uploader.getProgress() * 100);
                                break;
                            case MEDIA_COMPLETE:
                                System.out.println("‚úÖ Upload Complete");
                                break;
                            default:
                                System.out.println("‚ö†Ô∏è Upload State: " + uploader.getUploadState());
                                break;
                        }
                    }
            );

            // 5. Ïã§Ï†ú ÏóÖÎ°úÎìú ÏàòÌñâ
            com.google.api.services.drive.model.File uploadedFile = createRequest.execute();
            String fileId = uploadedFile.getId();

            System.out.println("‚úÖ File Uploaded: " + fileId);

            // 6. ÏóÖÎ°úÎìúÎêú ÌååÏùº Í≥µÍ∞ú Í∂åÌïú ÏÑ§Ï†ï
            setFilePublic(fileId);

            // 7. Í≥µÏú† Í∞ÄÎä•Ìïú Ïõπ Î∑∞ ÎßÅÌÅ¨ Î∞òÌôò
            return uploadedFile.getWebViewLink();

        } catch (Exception e) {
            System.err.println("‚ùå ÌååÏùº ÏóÖÎ°úÎìú Ïã§Ìå®: " + file.getOriginalFilename());
            e.printStackTrace();
            throw new RuntimeException("Google Drive upload failed", e);
        }
    }

    private void setFilePublic(String fileId) throws IOException {
        Permission permission = new Permission()
                .setType("anyone")               // ÎàÑÍµ¨ÎÇò Ï†ëÍ∑º Í∞ÄÎä•
                .setRole("reader")               // ÏùΩÍ∏∞ Í∂åÌïúÎßå
                .setAllowFileDiscovery(false);   // Í≤ÄÏÉâÏóîÏßÑÏóêÏÑú ÎÖ∏Ï∂ú Î∞©ÏßÄ

        driveService.permissions().create(fileId, permission)
                .setFields("id")
                .execute();

        System.out.println("üåç File is now public: " + fileId);
    }



